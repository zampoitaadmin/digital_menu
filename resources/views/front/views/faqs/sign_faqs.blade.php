							<p><strong>1) How do I install my sign?</strong><br>
                            Install your sign using our secret fitting system. To avoid ugly detailing, cracked signs when fitting and visible rusting screws we provide secret fixings on the BACK of all of our signs, we don’t drill holes right through the signs as standard. We sink into the back of the sign with one or more threaded pins, which are held rigid by an industrial resin.</p>
                            <p>Our reasons for doing this are as follows:<br>
                            – No ugly screws are visible.<br>
                            – Perfect for uneven surfaces.<br>
                            – Provides a super-fast process to install your sign.<br>
                            – Universal, threaded fixing for wall and gate attachment.</p>
                            <p>If we were to drill a hole through the slate, when it comes to screwing<br>
                            the sign to the wall you may slip and damage the front of your sign.</p>
                            <p>You don’t end up with weather marks on the front of your sign</p>
                            <p>All pins are tough, threaded nylon, not steel<br>
                            (as steel can rot and have a detrimental effect on your sign)</p>
